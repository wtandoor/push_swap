#include "libft.h"

int	ft_strlcpy(char *dst, const char *src, int size)
{
	int	i;
	int	sizesrc;

	if (dst == 0 || src == 0)
		return (0);
	sizesrc = ft_strlen(src);
	i = 0;
	if (size != 0)
	{
		while (src[i] != '\0' && i < (size - 1))
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (sizesrc);
}
